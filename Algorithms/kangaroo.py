# Complete the kangaroo function below.
def kangaroo(x1, v1, x2, v2):
    dx = x2 - x1
    dv = v1 - v2

    if dv <= 0:
        return "NO"
    else:
        return ("YES" if dx % dv == 0 else "NO")

if __name__ == '__main__':
    fptr = sys.stdout

    x1V1X2V2 = input().split()

    x1 = int(x1V1X2V2[0])

    v1 = int(x1V1X2V2[1])

    x2 = int(x1V1X2V2[2])

    v2 = int(x1V1X2V2[3])

    result = kangaroo(x1, v1, x2, v2)

    fptr.write(result + '\n')

    fptr.close()
